public class Kleiderschrank {
    int TShirts;
    int Hosen;
    int Socken;
    int Unterhosen;
    int Pullis;

    public Kleiderschrank(int TShirts, int Hosen, int Socken, int Unterhosen, int Pullis){
        this.TShirts = TShirts;
        this.Hosen = Hosen;
        this.Socken = Socken;
        this.Unterhosen = Unterhosen;
        this.Pullis = Pullis;
    }


}
